from random import randint

class Enemy :
    def __init__(self,name:str, pv:int,damage:int,dodge:int,ascii:str) :
        """
        the more dodge is high the more the enemy can dodge
        """
        self.name = name
        self.pv = pv
        self.damage = damage
        self.dodge = dodge
        self.ascii = ascii
        pass

    def __str__(self) :
        return f"{ascii}\nPV : {self.pv}"

    def try_dodge(self) :
        return randint(0,10) <= self.dodge

